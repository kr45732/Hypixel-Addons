package com.kr45732.hypixeladdons.mixins;

import com.kr45732.hypixeladdons.features.MysteryBoxOverlay;
import net.minecraft.client.network.NetHandlerPlayClient;
import net.minecraft.network.play.server.S2FPacketSetSlot;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(NetHandlerPlayClient.class)
public class MixinNetHandlerPlayClient {

	@Inject(method = "handleSetSlot", at = @At("RETURN"))
	public void handleSetSlot(S2FPacketSetSlot packetIn, CallbackInfo ci) {
		MysteryBoxOverlay.INSTANCE.setSlotPacket(packetIn);
	}
}
